-- PostgreSQL schema for atelier_shop (Neon 等平台需已创建好 atelier_shop 数据库)

DROP TABLE IF EXISTS order_item CASCADE;
DROP TABLE IF EXISTS shop_order CASCADE;
DROP TABLE IF EXISTS good CASCADE;
DROP TABLE IF EXISTS category CASCADE;
DROP TABLE IF EXISTS carousel CASCADE;
DROP TABLE IF EXISTS "user" CASCADE;
DROP TABLE IF EXISTS admin CASCADE;

CREATE TABLE "user" (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR(64) NOT NULL UNIQUE,
    email VARCHAR(128) NOT NULL UNIQUE,
    password VARCHAR(128) NOT NULL,
    nickname VARCHAR(64) NOT NULL DEFAULT '',
    phone VARCHAR(32) NOT NULL DEFAULT '',
    address VARCHAR(256) NOT NULL DEFAULT ''
);

CREATE TABLE admin (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR(64) NOT NULL UNIQUE,
    password VARCHAR(128) NOT NULL,
    role VARCHAR(32) NOT NULL DEFAULT 'admin',
    name VARCHAR(64) NOT NULL DEFAULT ''
);

CREATE TABLE category (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(64) NOT NULL,
    icon VARCHAR(32) NOT NULL DEFAULT '📁',
    description VARCHAR(256) NOT NULL DEFAULT ''
);

CREATE TABLE good (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(128) NOT NULL,
    price DECIMAL(10, 2) NOT NULL,
    category_id BIGINT NOT NULL,
    img VARCHAR(512) NOT NULL,
    description TEXT,
    stock INT NOT NULL DEFAULT 0,
    status VARCHAR(8) NOT NULL DEFAULT 'on',
    hot BOOLEAN NOT NULL DEFAULT FALSE
);

CREATE INDEX idx_good_category ON good (category_id);

CREATE TABLE shop_order (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    order_no VARCHAR(32) NOT NULL UNIQUE,
    create_time TIMESTAMP NOT NULL,
    pay_time TIMESTAMP NULL,
    status INT NOT NULL DEFAULT 0,
    total_price DECIMAL(10, 2) NOT NULL,
    address VARCHAR(256) NOT NULL,
    receiver VARCHAR(64) NOT NULL,
    phone VARCHAR(32) NOT NULL,
    logistics_company VARCHAR(64) NULL,
    tracking_no VARCHAR(64) NULL,
    logistics_status VARCHAR(64) NULL
);

CREATE INDEX idx_order_user ON shop_order (user_id);

CREATE TABLE order_item (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_id BIGINT NOT NULL,
    good_id BIGINT NOT NULL,
    count INT NOT NULL,
    price DECIMAL(10, 2) NOT NULL
);

CREATE INDEX idx_order_item_order ON order_item (order_id);

CREATE TABLE carousel (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR(128) NOT NULL,
    subtitle VARCHAR(256) NOT NULL DEFAULT '',
    img VARCHAR(512) NOT NULL,
    link VARCHAR(256) NOT NULL DEFAULT '/',
    sort_order INT NOT NULL DEFAULT 0
);
